import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const mobileRoot = path.resolve(__dirname, '..');
const projectRoot = path.resolve(mobileRoot, '..');
const webDir = path.join(mobileRoot, 'www');
const envFilePath = path.join(mobileRoot, '.env');
const defaultApiBaseUrl = 'http://localhost:4100';

const filesToCopy = [
  'index.html',
  'staff.html',
  'admin.html',
  'styles.css',
  'app.js',
  'staff.js',
  'admin.js',
  'data.js'
];

async function loadEnvFile() {
  try {
    const raw = await fs.readFile(envFilePath, 'utf8');
    return raw
        .split(/\r?\n/)
        .map((line) => line.trim())
        .filter((line) => line && !line.startsWith('#'))
        .reduce((env, line) => {
          const separatorIndex = line.indexOf('=');
          if (separatorIndex === -1) {
            return env;
          }

          const key = line.slice(0, separatorIndex).trim();
          env[key] = line.slice(separatorIndex + 1).trim().replace(/^['"]|['"]$/g, '');
          return env;
        }, {});
  } catch (error) {
    if (error.code === 'ENOENT') {
      return {};
    }
    throw error;
  }
}

function resolveApiBaseUrl(envFile) {
  const configuredUrl = process.env.MOBILE_API_BASE_URL
      || process.env.CAFE_API_BASE_URL
      || envFile.MOBILE_API_BASE_URL
      || envFile.CAFE_API_BASE_URL
      || defaultApiBaseUrl;

  return configuredUrl.trim().replace(/\/$/, '');
}

function createConfigContent(apiBaseUrl) {
  return `// Generated by mobile/scripts/sync-web-assets.mjs\n// Set MOBILE_API_BASE_URL in mobile/.env for staging or production builds.\nwindow.CAFE_API_BASE_URL = window.CAFE_API_BASE_URL || '${apiBaseUrl}';\n`;
}

async function resetWebDir() {
  await fs.rm(webDir, { recursive: true, force: true });
  await fs.mkdir(webDir, { recursive: true });
}

function injectConfigTag(html) {
  if (html.includes('src="config.js"') || html.includes('src="./config.js"')) {
    return html;
  }

  return html.replace(/<script\s+src="(app|staff|admin)\.js"\s*><\/script>/, (_match, scriptName) => {
    return `<script src="./config.js"></script>\n  <script src="./${scriptName}.js"></script>`;
  });
}

async function copyWebAssets(configContent) {
  for (const relativeFile of filesToCopy) {
    const src = path.join(projectRoot, relativeFile);
    const dest = path.join(webDir, relativeFile);
    const content = await fs.readFile(src, 'utf8');

    if (relativeFile.endsWith('.html')) {
      await fs.writeFile(dest, injectConfigTag(content), 'utf8');
    } else {
      await fs.writeFile(dest, content, 'utf8');
    }
  }

  await fs.writeFile(path.join(webDir, 'config.js'), configContent, 'utf8');
}

async function main() {
  const envFile = await loadEnvFile();
  const apiBaseUrl = resolveApiBaseUrl(envFile);
  const configContent = createConfigContent(apiBaseUrl);

  await resetWebDir();
  await copyWebAssets(configContent);
  console.log('Mobile web assets synced to:', webDir);
  console.log('Mobile API base URL:', apiBaseUrl);
}

main().catch((error) => {
  console.error('Failed to sync web assets:', error);
  process.exit(1);
});


